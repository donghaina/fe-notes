var map = document.querySelector('.map');
function createMap() {
  var i = 0;
  var len = mapArray.length;
  // console.log(len);
  var cols = Math.sqrt(len);
  var cellSize = 30;
  for (i; i < len; i++) {
    var li = document.createElement('li');
    li.style.width = cellSize+'px';
    li.style.height = cellSize+'px';

    switch (mapArray[i]){
      case 1:
        li.className = 'red';
        break;
      case 2:
        li.className = 'green';
        break;
      case 3:
        li.className = 'blue';
        break;
      default:
        li.className = '';
    }
    map.appendChild(li);
  }
  map.style.width = ((cellSize+1) * cols)+1+'px';
}

